
.ORIG x3000

LD R0, xA      ; L0AD R0 WITH X4000, SO R0 IS THE POINTER
LDR R1, R0, #0 ; LOAD NUMBER 1 INTO R1
LDR R2, R0, #1 ; LOAD NUMBER 2 INTO R2

LD R3, x8       ; LOAD MASK X00FF INTO R3
AND R1, R1, R3  ; BITWISE AND BITS [15:8]
AND R2, R2, R3

ADD R4, R1, R2

NOT R4, R4
ADD R4, R4, #1

STR R4, R0, #2

HALT

.FILL x4000
.FILL x00FF

.END